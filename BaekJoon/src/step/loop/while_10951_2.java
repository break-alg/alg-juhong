import java.util.StringTokenizer;

public class while_10951_2 {
    public static void main(String[] args) throws IOException {

        Scanner sc = new Scanner(System.in);
        int x, y;

        while(sc.hasNext()) {
            x = sc.nextInt();
            y = sc.nextInt();

            System.out.println(x+y);
        }


    }
}
